package com.booking.platform.mybookingshow.models;

public class AtomicPersistResponse {

	boolean status;
	
	String message;

	public boolean isStatus() {
		return status;
	}

	public void setStatus(boolean status) {
		this.status = status;
	}

	public String isMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}
	
	

}
